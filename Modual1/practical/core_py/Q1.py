# Write a Python program to demonstrate the creation of variables and different data types.

age = 25
print("Age (Integer):", age)

height = 5.9
print("Height (Float):", height)

name = "John Doe"
print("Name (String):", name)

is_student = True
print("Is Student (Boolean):", is_student)

fruits = ["apple", "banana", "cherry"]
print("Fruits (List):", fruits)

coordinates = (10, 20)
print("Coordinates (Tuple):", coordinates)

person = {"name": "Alice", "age": 30, "city": "New York"}
print("Person (Dictionary):", person)

unique_numbers = {1, 2, 3, 4, 5}
print("Unique Numbers (Set):", unique_numbers)

nothing = None
print("Nothing (None):", nothing)


