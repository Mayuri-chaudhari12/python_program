# Practical Example 4: How to check the type of a variable dynamically using type().

var1 = 42                 
var2 = 3.14              
var3 = "Hello, World!"    
var4 = [1, 2, 3, 4]       
var5 = True               


print("The type of var1 is:", type(var1))  # Output: <class 'int'>
print("The type of var2 is:", type(var2))  # Output: <class 'float'>
print("The type of var3 is:", type(var3))  # Output: <class 'str'>
print("The type of var4 is:", type(var4))  # Output: <class 'list'>
print("The type of var5 is:", type(var5))  # Output: <class 'bool'>
