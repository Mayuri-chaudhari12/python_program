# Write a Python program to access values using dictionary keys.

my_dict = {
    "name": "John",
    "age": 30,
    "city": "New York",
    "profession": "Engineer",
    "hobby": "Reading",
    "country": "USA"
}

name = my_dict["name"]
age = my_dict["age"]
city = my_dict["city"]
profession = my_dict["profession"]
hobby = my_dict["hobby"]
country = my_dict["country"]

print("Name:", name)
print("Age:", age)
print("City:", city)
print("Profession:", profession)
print("Hobby:", hobby)
print("Country:", country)
