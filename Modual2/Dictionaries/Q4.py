
# Write a Python program to access values using keys from a dictionary.

car_dict = {
    "brand": "Toyota",
    "model": "Corolla",
    "year": 2020,
    "color": "Blue",
    "fuel_type": "Gasoline",
    "price": 20000
}

brand = car_dict["brand"]
model = car_dict["model"]
year = car_dict["year"]
color = car_dict["color"]
fuel_type = car_dict["fuel_type"]
price = car_dict["price"]

print("Brand:", brand)
print("Model:", model)
print("Year:", year)
print("Color:", color)
print("Fuel Type:", fuel_type)
print("Price:", price)
